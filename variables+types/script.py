#!/usr/bin/python3

# Numebrs
myInt = 7
print(myInt)

myFloat = 7.0
print(myFloat)

# Strings
myString = 'hello'
print(myString)

myString = "Don't worry about apostrophes!"
print(myString)

# Combining Strings
hello = 'Hello'
world = 'Mom'
helloWorld = hello + ', ' + world + '!'

print(helloWorld)

# Assign more than one var
a, b = 3, 4
print(a, b)
